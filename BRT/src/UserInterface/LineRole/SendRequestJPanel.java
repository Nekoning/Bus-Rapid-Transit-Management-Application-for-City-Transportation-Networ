    public void populateVolumnTable() {
        DefaultTableModel dtm = (DefaultTableModel) volumnJTable.getModel();
        dtm.setRowCount(0);

        for (Employee employee : platformOrganization.getEmployeeDirectory().getEmployeeList()) {
            if (employee instanceof Platform) {
                Platform platform = (Platform) employee;
                Object row[] = new Object[2];

                row[0] = platform;
                row[1] = platform.getVolumn();

                dtm.addRow(row);
            }

        }
    }
    
    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) requestJTable.getModel();        
        model.setRowCount(0);
        
        for (WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            DispatchBusWorkRequest request = (DispatchBusWorkRequest) workRequest;
            row[0] = ((Platform) request.getPlatform());
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            String result = ((DispatchBusWorkRequest) request).getTestResult();
            row[3] = result == null ? "Waiting" : result;
            row[4] = request.getRequestTime();
            row[5] = request.getResolveTime();
            
            model.addRow(row);
        }
    }
    

