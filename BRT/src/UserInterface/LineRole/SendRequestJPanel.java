    private void requestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestJButtonActionPerformed
        // TODO add your handling code here:
        Date date = new Date();
        int selectedRow = volumnJTable.getSelectedRow();
        if (selectedRow >= 0) {
            Platform platform = (Platform) volumnJTable.getValueAt(selectedRow, 0);
            DispatchBusWorkRequest request = new DispatchBusWorkRequest();
            //request.setMessage(message);
            request.setSender(userAccount);
            request.setStatus("Sent");
            request.setPlatform(platform);
            request.setRequestTime(date);

            Organization org = null;
            for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (organization instanceof RegulationOrganization) {
                    org = organization;
                    break;
                }
            }

            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(request);
                userAccount.getWorkQueue().getWorkRequestList().add(request);
                populateRequestTable();
            } else {
                JOptionPane.showMessageDialog(null, "You don't have a RegulationOrganization yet.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_requestJButtonActionPerformed

