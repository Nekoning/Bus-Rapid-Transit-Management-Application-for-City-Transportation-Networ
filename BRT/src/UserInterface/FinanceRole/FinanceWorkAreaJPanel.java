    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) requestJTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest workRequest : financeOrganization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            PurchaseTestWorkRequest request = (PurchaseTestWorkRequest) workRequest;
            row[0] = request;
            row[1] = request.getReceiver()== null ? null : request.getReceiver().getEmployee().getName();
            row[2] = request.getSender().getEmployee().getName();
            row[3] = request.getStatus();
            row[4] = request.getTestResult();
            row[5] = request.getNotice();
            
            model.addRow(row);
        }
    }

