    private void assginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assginJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row first");
        }

        WorkRequest workRequest = (WorkRequest)requestJTable.getValueAt(selectedRow, 0);
        workRequest.setReceiver(userAccount);
        workRequest.setStatus("Pending");

        populateRequestTable();
    }//GEN-LAST:event_assginJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestJTable.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }

        PurchaseTestWorkRequest request = (PurchaseTestWorkRequest) requestJTable.getValueAt(selectedRow, 0);

        request.setStatus("Processing");

        ProcessFinanceRequestJPanel panel = new ProcessFinanceRequestJPanel(userProcessContainer, request);
        userProcessContainer.add("ProcessFinanceRequestJPanel", panel);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
    }//GEN-LAST:event_processJButtonActionPerformed


